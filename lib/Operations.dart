double divide(double first, double second){
  double result = first /second;
  return result;
}

double multiply(double first, double second){
  double result = first * second;
  return result;
}

double add(double first, double second){
  double result = first + second;
  return result;
}

double subtract(double first, double second){
  double result = first - second;
  return result;
}
